        public string ID { get; set; }

        [Required]
        //[EmailAddress]        
        [Display(Name = "Email")]
        [RegularExpression(pattern, ErrorMessage = "Email is not valid.")]
        public string Email { get; set; }
        public string UserName { get; set; }
